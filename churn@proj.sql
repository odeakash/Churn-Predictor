#Rank regions based on average revenue per customer.
SELECT Region, AVG(TotalSpend) AS AvgRevenuePerCustomer
FROM (
    SELECT C.CustomerID, Region, SUM(Amount) AS TotalSpend
    FROM Customers C
    JOIN Transactions T 
    ON C.CustomerID = T.CustomerID
    GROUP BY CustomerID, Region
) AS CustomerSpending
GROUP BY Region
ORDER BY AvgRevenuePerCustomer DESC;

