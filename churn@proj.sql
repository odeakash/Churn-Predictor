#Calculate the monthly revenue growth over the last year.

SELECT 
    DATE_FORMAT(STR_TO_DATE(TransactionDate, '%d-%m-%Y'), '%Y-%m') AS Month,
    SUM(Amount) AS MonthlyRevenue
FROM Transactions
WHERE STR_TO_DATE(TransactionDate, '%d-%m-%Y') >= DATE_SUB(CURDATE(), INTERVAL 1 YEAR)
GROUP BY Month
ORDER BY Month;